#位置参数
def f1(a,b,c):
    print(a,b,c)
f1(2,3,4)
#f1(2,3) #报错，位置参数不匹配
print("*************************")

#默认值参数
def f1(a,b,c=10,d=20): #默认值参数必须位于普通位置参数后面
    print(a,b,c,d)
f1(8,9)
f1(8,9,19)
f1(8,9,19,29)
print("*************************")

#命名参数
def f1(a,b,c):
    print(a,b,c)
f1(8,9,19)         #位置参数
f1(c=10,a=20,b=30) #命名参数
print("*************************")